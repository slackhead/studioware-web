<a name="about-the-studioware-project" class="anchor" href="#about-the-studioware-project"><span class="octicon octicon-link"></span></a>The Studioware Project</h3>

<p>Studioware is a project aimed at providing build scripts and packages of the best open source audio, video and photo editing software available for Slackware Linux.
It includes libraries and other dependencies with a comprehensive dependency list for each package so you will know exactly what you need to install.
Although Studioware is quite a small project, the maintainers are Slackware users of many years experience. The wiki is being built up steadily and is the main source of the project's <a href="http://docs.slackware.com/studioware:start">documentation</a> 
and is hosted by the Slackware Documentation Project. See the <a href="http://docs.slackware.com/studioware:quick_start">Quick Start</a> guide to get started.
</p>

